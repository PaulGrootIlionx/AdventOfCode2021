declare @hpos int = 0;
declare @depth int = 0;
declare @action nvarchar(1);
declare @value int;
declare @result int;

declare cur cursor for
select
	left(command,1) as [action]
	,REVERSE(left(REVERSE(command),charindex(' ',REVERSE(command))-1)) as [value]
from #input
order by
	AutoID;

open cur;

fetch next from cur into @action, @value;

while @@FETCH_STATUS = 0
begin
	--submarine moves forward
	if (@action = 'f')
	begin
		set @hpos = @hpos + @value;
	end
	--submarine submerges
	if (@action = 'd')
	begin
		set @depth = @depth + @value;
	end
	--submarine ascends
	if (@action = 'u')
	begin
		set @depth = @depth - @value;
	end

	fetch next from cur into @action, @value;
end;

close cur;
deallocate cur;

drop table #input;
print concat('depth: ',@depth);
print concat('hpos: ',@hpos);
set @result = @hpos*@depth;

