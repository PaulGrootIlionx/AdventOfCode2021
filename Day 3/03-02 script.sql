	set @pos = @pos + 1;
end

create table #temp ([val] nvarchar(1), [counts] int)
declare @count0 int;
declare @count1 int;

--Get oxygen rating
create table #oxygen ([value] nvarchar(12));

insert into #oxygen
select
	[value]
from #input;

set @pos = 1;
while ((select COUNT(*) from #oxygen) > 1 )
begin
	insert into #temp
	select
		SUBSTRING([value],@pos,1), COUNT(*)
	from #oxygen
	group by
		SUBSTRING([value],@pos,1);

	select @count0 = [counts] from #temp where [val] = '0';
	select @count1 = [counts] from #temp where [val] = '1';

	set @val = '';
	if (isnull(@count0,0) = isnull(@count1,0))
	begin
		set @val = '1';
	end
	if (isnull(@count0,0) > isnull(@count1,0))
	begin
		set @val = '0';
	end
	if (isnull(@count0,0) < isnull(@count1,0))
	begin
		set @val = '1';
	end

	delete from #oxygen where SUBSTRING([value],@pos,1) = @val;
	truncate table #temp;

	set @pos = @pos + 1;
end

